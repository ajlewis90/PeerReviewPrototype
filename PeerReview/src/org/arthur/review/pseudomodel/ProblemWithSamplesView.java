package org.arthur.review.pseudomodel;

public class ProblemWithSamplesView {

	private String problemNumber;
	private String firstSampleSolution;
	private String secondSampleSolution;
	private String thirdSampleSolution;
	
	public String getProblemNumber() {
		return problemNumber;
	}
	
	public void setProblemNumber(String problemNumber) {
		this.problemNumber = problemNumber;
	}
	
	public String getFirstSampleSolution() {
		return firstSampleSolution;
	}
	
	public void setFirstSampleSolution(String firstSampleSolution) {
		this.firstSampleSolution = firstSampleSolution;
	}
	
	public String getSecondSampleSolution() {
		return secondSampleSolution;
	}
	
	public void setSecondSampleSolution(String secondSampleSolution) {
		this.secondSampleSolution = secondSampleSolution;
	}
	
	public String getThirdSampleSolution() {
		return thirdSampleSolution;
	}
	
	public void setThirdSampleSolution(String thirdSampleSolution) {
		this.thirdSampleSolution = thirdSampleSolution;
	}
	
}
